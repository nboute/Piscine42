int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int sols;

	i = 0;
	sols = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			sols++;
		i++;
	}
	return (sols);
}
