int		ft_is_prime(int nb)
{
	int div;

	div = 3;
	if (nb == 2)
		return (1);
	if (nb <= 1 || (nb % 2 == 0))
		return (0);
	while (div <= nb / div)
	{
		if (nb % div == 0)
			return (0);
		else
			div += 2;
	}
	return (1);
}
