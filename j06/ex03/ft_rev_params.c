void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int i;
	int j;

	j = argc - 1;
	while (j > 0)
	{
		i = 0;
		while (argv[j][i])
		{
			ft_putchar(argv[j][i]);
			i++;
		}
		ft_putchar('\n');
		j--;
	}
}
